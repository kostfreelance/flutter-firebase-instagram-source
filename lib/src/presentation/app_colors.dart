import 'package:flutter/material.dart';

abstract class AppColors {
  static Color get black => const Color(0xff000000);
  static Color get white => const Color(0xffFFFFFF);
  static Color get grey => const Color(0xff616161);
  static Color get lightGrey => const Color(0xffC6C6C6);
  static Color get veryLightGrey => const Color(0xffFAFAFA);
  static Color get blue => const Color(0xff2663FF);
  static Color get red => Colors.red;
}